	<h1>Frequently Asked Questions</h1>
	<div class="column1 full">
	<h5>What is a lease?</h5>
	<p>A lease is a contract where one party (Lessor) allows another (Lessee) the exclusive right to use and possess its equipment, including vehicles, for a specific period of time. The contract obligates the Lessee to make periodic payments to the Lessor for the use of the equipment. A lease, utilized as a source of financing, is usually a long-term agreement that is non-cancelable. The Lessee is responsible for all peripheral costs associated with the use of the equipment, including taxes, insurance and maintenance during the term of the lease. At the end of the lease term, the Lessee may have an option to extend the lease term, return the equipment or purchase it at a predetermined option price.</p>

	<h5>What is the difference between a lease and a loan?</h5>
	<p>The Lessor retains legal ownership of the equipment during the term of the lease.</p>

	<h5>Why Lease?</h5>
	<p>Affordable payments…potential tax benefits liabilities…preservation of lines of credit or retention of valuable cash. Thanks to benefits like these, eight out of ten U.S. businesses use lease financing to acquire the vehicles and equipment they need to grow.</p>

	<h5>Who can lease?</h5>
	<p>Any company, organization or association and all municipal, state and government agencies can apply for leasing. We do not lease to individuals for personal use.</p>

	<h5>What type of Equipment does Dealers Leasing lease/finance?</h5>
	<p>Any new or used equipment acquired for the production of income or to decrease operating expenses can probably be leased from us. This would include telephone systems, computers and all types of fleet cars, over the road tractors and trailers and commercial trucks and vans.</p>

	<h5>Can I lease software?</h5>
	<p>Yes. Dealers Leasing can provide up to 100% of the cost of your next software upgrade including installation costs.</p>

	<h5>How do I apply for a lease?</h5>
	<p>Applying for a lease is easy. You can fax our on-line application to us or just call and speak to one of our account managers to get started.</p>

	<h5>How long does it take to be approved?</h5>
	<p>Depending on complexity, completed applications with appropriate financial documentation will generally have an approval within 24-hours.</p>

	<h5>How is my monthly payment calculated?</h5>
	<p>For business equipment, a monthly lease payment is determined by a Lease Rate Factor: a periodic rental payment to a lessor for the use of the assets. The Lease Rate Factor for the desired lease term is multiplied by the equipment cost to determine the monthly payment. Factors differ for leases with lease-end residual balances from those with a $1.00 option. In the case of equipment, the lessee shops and determines the cost of the equipment to be leased.</p>

	<p>Vehicle lease payments vary based on the type of vehicle, usage and term. Each is custom quoted. Unlike equipment leasing, Dealers Leasing should be involved in locating and acquiring the vehicles(s) to take advantage of our purchasing power. Factory orders, if time allows, are generally less expensive.</p>

	<h5>Do I need to have insurance on leased equipment? Am I responsible if the equipment is damaged or destroyed?</h5>
	<p>Full coverage insurance, both liability and physical damage, is required on all leased equipment. The Lessee is responsible to keep all leased assets in original condition except normal wear and tear.</p>

	<h5>When does the lease start?</h5>
	<p>The lease starts when the leased asset is delivered and we receive confirmation that it is in good working order and meets your expectations. The initial payment is pro-rated to the end of the month with the remaining payments due on the first day of each month.</p>

	<h5>Can equipment or vehicles be added to a lease at a later date?</h5>
	<p>Yes. It is as easy as setting up another lease with the payment added to your monthly statement.</p>

	<h5>Who owns leased equipment?</h5>
	<p>Dealers Leasing, as lessor, is the owner of the equipment until the end of the term.</p>

	<h5>What is the interest rate in a lease?</h5>
	<p>Lease rates are different than interest rates. Lease rates are for determining the base lease payment and can be compared to other lease payments.There is no "interest" in a lease payment. You are paying for the use of the asset over the lease term. The payment varies based on which lease plan you choose, the term of the lease and the cost of the equipment.</p>

	<h5>Can a lease be cancelled? Can I pay off my lease early?</h5>
	<p>A lease is a non-cancelable contract. However, if you want to terminate the lease early, the remaining lease payments are generally discounted to arrive at a lease payoff balance.</p>

	<h5>What is the buyout at the end of the lease?</h5>
	<p>You have a variety of options available to you in structuring what happens at the end of the lease term.  Depending on the leased asset, we have everything from a $1.00 buyout to a fixed amount due which is guaranteed in the purchase agreement. These options are pre-determined at the beginning of the lease so that you know exactly what you are going to pay. We can also structure some vehicle leases with a Fair Market Value option, not an obligation, at the end of the lease if that better suits your needs.</p>

	<h5>Are lease payments a tax write off?</h5>
	<p>Most all vehicle leases and some equipment leases can be structured so that the lease payment can be written off as an operating expense. It is recommended that you consult with your tax advisor for the specific application to your business.</p>

	<h5>Are the lease rates variable?</h5>
	<p>Lease rates are fixed for the term of the lease regardless of what happens in the banking industry or on Wall Street.</p>

	<h5>Can service and installation costs be added to a lease?</h5>
	<p>Yes. Installation and service fees, like software, can normally be included in the lease.</p>

	<h5>I have a new corporation and need equipment. What type of paper work will be required to lease?</h5>
	<p>New corporations are subject to additional documentation requirements beyond the normal lease application. These may include two years personal tax returns and financial statements for each principal and occasionally a completed business plan.</p>

	<h5>Can I lease used equipment?</h5>
	<p>Yes. In most cases used vehicles or equipment can be leased.</p>

	<h5>Can you purchase my existing equipment and lease it back to me?</h5>
	<p>In many cases, a sale/leaseback can be arranged depending on the reasons for the transaction, age and value of the equipment.</p>
